#include <stdio.h>

int main() {
    int a = 16;
    int b = 3;

    // 1. Addition
    printf("a + b = %d\n", a + b);

    // 2. Soustraction
    printf("a - b = %d\n", a - b);

    // 3. Multiplication
    printf("a * b = %d\n", a * b);

    // 4. Division
    printf("a / b = %d\n", a / b);

    // 5. Modulo
    printf("a %% b = %d\n", a % b);

    // 6. Comparaison d'égalité
    printf("a == b : %d\n", a == b);

    // 7. Comparaison de supériorité
    printf("a > b : %d\n", a > b);

    return 0;
}
